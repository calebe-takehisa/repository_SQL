/*LISTA DE ITENS COM PREÇOS ENTRE 1000 ATÉ 1500*/
SELECT *
FROM Production.Product
WHERE ListPrice BETWEEN 1000 AND 1500;

/*LISTA DE ITENS COM PREÇOS QUE NÃO ESTEJAM ENTRE 1000 E 1500*/
SELECT *
FROM Production.Product
WHERE ListPrice NOT BETWEEN 1000 AND 1500;

/*LISTA DE FUNCIONARIOS CONTRATADOS ENTRE 2009 E 2010*/ 
SELECT *
FROM HumanResources.Employee
WHERE HireDate BETWEEN '2009/01/01' AND '2010/01/01'
ORDER BY HireDate